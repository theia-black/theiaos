#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
IMAGE_NAME="${THEIAOS_INSTALL_E2E_IMAGE:-${CLAWDBOT_INSTALL_E2E_IMAGE:-theiaos-install-e2e:local}}"
INSTALL_URL="${THEIAOS_INSTALL_URL:-${CLAWDBOT_INSTALL_URL:-https://theiaos.bot/install.sh}}"

OPENAI_API_KEY="${OPENAI_API_KEY:-}"
ANTHROPIC_API_KEY="${ANTHROPIC_API_KEY:-}"
ANTHROPIC_API_TOKEN="${ANTHROPIC_API_TOKEN:-}"
THEIAOS_E2E_MODELS="${THEIAOS_E2E_MODELS:-${CLAWDBOT_E2E_MODELS:-}}"

echo "==> Build image: $IMAGE_NAME"
docker build \
  -t "$IMAGE_NAME" \
  -f "$ROOT_DIR/scripts/docker/install-sh-e2e/Dockerfile" \
  "$ROOT_DIR/scripts/docker/install-sh-e2e"

echo "==> Run E2E installer test"
docker run --rm \
  -e THEIAOS_INSTALL_URL="$INSTALL_URL" \
  -e THEIAOS_INSTALL_TAG="${THEIAOS_INSTALL_TAG:-${CLAWDBOT_INSTALL_TAG:-latest}}" \
  -e THEIAOS_E2E_MODELS="$THEIAOS_E2E_MODELS" \
  -e THEIAOS_INSTALL_E2E_PREVIOUS="${THEIAOS_INSTALL_E2E_PREVIOUS:-${CLAWDBOT_INSTALL_E2E_PREVIOUS:-}}" \
  -e THEIAOS_INSTALL_E2E_SKIP_PREVIOUS="${THEIAOS_INSTALL_E2E_SKIP_PREVIOUS:-${CLAWDBOT_INSTALL_E2E_SKIP_PREVIOUS:-0}}" \
  -e OPENAI_API_KEY="$OPENAI_API_KEY" \
  -e ANTHROPIC_API_KEY="$ANTHROPIC_API_KEY" \
  -e ANTHROPIC_API_TOKEN="$ANTHROPIC_API_TOKEN" \
  "$IMAGE_NAME"
